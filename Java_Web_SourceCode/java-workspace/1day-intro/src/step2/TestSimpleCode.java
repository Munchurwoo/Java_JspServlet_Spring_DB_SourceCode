package step2;

/*  클래스 선언부 
 *   public : 접근제어자 access modifier  어디서나 접근 가능 
 *   class : 클래스 정의시 사용하는 자바 예약어 ( keyword ) 
 *   TestSimpleCode : 클래스명 
 *   							대문자로 시작하고 합성어 첫글자 대문자
 *    {  클래스 시작 
 *    
 *    }  클래스 종료 
 */
public class TestSimpleCode {
	/**
	 * 메인 메서드 : java 프로그램 실행시 시작점 
	 * @param args
	 */
	public static void main(String[] args) {
		//출력 메서드 호출 
		System.out.println("안녕 자바");
		// 프로그램 실행 ctrl + f11
	}
}









